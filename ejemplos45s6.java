import java.util.Scanner;

public class ejemplos45s6 {
     public static void main(String[] args) {
      int first, second, Suma, Resta, Multiplicación;
      float División;
      Scanner scanner = new Scanner(System.in);
      System.out.print("Ingrese dos números: ");
      first = scanner.nextInt();
      second = scanner.nextInt();
      Suma = first + second;
      Resta = first - second;
      Multiplicación = first * second;
      División = first / second;
      System.out.println("Suma = " + Suma);
      System.out.println("Resta = " + Resta);
      System.out.println("Multiplicación = " + Multiplicación);
      System.out.println("División = " + División);
        

    
}
}    
