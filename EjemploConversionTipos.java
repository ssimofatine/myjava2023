public class EjemploConversionTipos {
    public static void main(String[] args) {
        int numro = 65;
        System.out.println(numro);
        System.out.println((char) numro);
        //
        double precio = 25;
        System.out.println((int) precio); //
        System.out.println(Math.ciel(precio)); //
        System.out.println(Math.floor(precio)); //
    }
}
